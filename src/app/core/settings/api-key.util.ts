/**
 * @file Gemini API キーの正規化を行う純粋関数。
 * 貼り付け時に混入しがちな前後の空白・改行・引用符を除去する。
 * プレフィックスによる形式判定は行わない — Google はキー形式を移行中で（従来の "AIza" に対し、
 * 新規発行キーは "AQ." で始まる）、アプリ側でプレフィックスを検査すると正常なキーを誤って警告するため。
 * キーの有効性は送信時に Google が返す 401/403 を gemini-error.util.ts で日本語化して伝える。
 */

// ── 正規化: 貼り付け時に混入する不可視文字・囲み文字を除去 ──────────
// 対象は前後の半角/全角空白・改行・タブと、キー全体を囲むシングル/ダブルクォート。
// キー内部の文字は一切変更しない（Google がキー文字種を変えても壊れないようにするため）。
export function normalizeApiKey(raw: string): string {
  return raw
    .replace(/^[\s　]+|[\s　]+$/g, '')
    .replace(/^["'](.*)["']$/s, '$1')
    .replace(/^[\s　]+|[\s　]+$/g, '');
}
